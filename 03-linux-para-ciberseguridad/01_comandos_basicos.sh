#!/bin/bash

# ==============================================================================
#           COMANDOS BÁSICOS DE LINUX: TU NUEVO VOCABULARIO
# ==============================================================================
#
# Aprender comandos es como aprender verbos en un idioma. 
# Aquí tienes los más importantes explicados como acciones de la vida real.
# ---

echo "--- BIENVENIDO A LA TERMINAL ---"

# 1. pwd (Print Working Directory)
# Analogía: Mirar a tu alrededor y preguntar "¿Dónde estoy?".
# Te dice en qué habitación (carpeta) estás parado.
echo "[1] ¿Dónde estoy ahora? -> Comando: pwd"

# 2. ls (List)
# Analogía: Encender la luz de la habitación para ver qué hay.
# Lista los archivos y carpetas que tienes cerca.
echo "[2] ¿Qué hay en esta habitación? -> Comando: ls"

# 3. cd (Change Directory)
# Analogía: Caminar de una habitación a otra.
# Te permite moverte por las carpetas.
echo "[3] Moverme a la cocina -> Comando: cd cocina"

# 4. mkdir (Make Directory)
# Analogía: Construir una nueva habitación en la casa.
# Crea una carpeta nueva.
echo "[4] Crear una carpeta llamada 'secretos' -> Comando: mkdir secretos"

# 5. touch
# Analogía: Crear un papel en blanco sobre la mesa.
# Crea un archivo vacío.
echo "[5] Crear un archivo de texto -> Comando: touch nota.txt"

# 6. rm (Remove)
# Analogía: Tirar algo a la basura (¡Cuidado, no hay papelera de reciclaje!).
# Borra archivos o carpetas para siempre.
echo "[6] Borrar la nota -> Comando: rm nota.txt"

# 7. sudo (SuperUser DO)
# Analogía: Sacar el carnet de identidad del dueño de la casa.
# Te da permisos para hacer cosas que un usuario normal no puede.
echo "[7] Hacer algo como JEFE -> Comando: sudo ..."

echo "-------------------------------------------------------"
echo "TRUCO: Si te pierdes, escribe 'man' seguido del comando."
echo "Es como pedirle el manual de instrucciones a la computadora."
echo "-------------------------------------------------------"
